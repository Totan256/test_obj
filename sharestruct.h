#ifndef SHARESTRUCT_H
#define SHARESTRUCT_H

struct vec3
{
    float x=0.0;
    float y=0.0;
    float z=0.0;
};
struct ivec3
{
    int x=0.0;
    int y=0.0;
    int z=0.0;
};
struct vec2{
    float x = 0.0;
    float y = 0.0;
};
struct ivec2{
    int x = 0.0;
    int y = 0.0;
};
#endif //SHARESTRUCT_H